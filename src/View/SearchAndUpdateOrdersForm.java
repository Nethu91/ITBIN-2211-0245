    private void srcBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_srcBtnActionPerformed
        // TODO add your handling code here:
       try{
           Statement s =DBConnection.getStatementConnection();
           ResultSet rs =s.executeQuery("SELECT * FROM orders WHERE order_ID = '"+dIDTF.getText()+"'");
           if(rs.next()){
               orderDTF.setText(rs.getString("order_Date"));
               ammountTF.setText(rs.getString("total_Ammount"));
               cNamTF.setText(rs.getString("cus_Name"));
               
           }
       }catch(SQLException e){
           System.out.println(e);
           JOptionPane.showMessageDialog(this, "Invalid Account Number: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_srcBtnActionPerformed

